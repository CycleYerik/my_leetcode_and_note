class Solution {
    public:
        int reverseBits(int n) {
            uint32_t result = 0;
    
            for(int i = 0 ; i < 32 ; i ++)
            {
                result <<= 1;
                result |= (n & 1U);
    
                n >>= 1;
    
            }
            return result;
        }
    };

    class Solution {
        public:
            uint32_t reverseBits(uint32_t n) {
                // 第一步：交换高低 16 位
                // 不需要掩码，因为移位后超出的部分会自动丢弃
                n = (n >> 16) | (n << 16);
        
                // 第二步：每 8 位为一组，组内高低 4 位交换
                // 0xFF00FF00 对应二进制: 11111111 00000000 11111111 00000000
                n = ((n & 0xFF00FF00) >> 8) | ((n & 0x00FF00FF) << 8);
        
                // 第三步：每 4 位为一组，组内高低 2 位交换
                // 0xF0F0F0F0 对应二进制: 11110000 11110000 11110000 11110000
                n = ((n & 0xF0F0F0F0) >> 4) | ((n & 0x0F0F0F0F) << 4);
        
                // 第四步：每 2 位为一组，组内高低 1 位交换
                // 0xCCCCCCCC 对应二进制: 11001100 11001100 11001100 11001100
                n = ((n & 0xCCCCCCCC) >> 2) | ((n & 0x33333333) << 2);
        
                // 第五步：相邻位互换
                // 0xAAAAAAAA 对应二进制: 10101010 10101010 10101010 10101010
                // 0x55555555 对应二进制: 01010101 01010101 01010101 01010101
                n = ((n & 0xAAAAAAAA) >> 1) | ((n & 0x55555555) << 1);
        
                return n;
            }
        };