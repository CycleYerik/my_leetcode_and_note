#include <cstdlib>   // malloc, free
#include <cstdint>   // uintptr_t
#include <cstddef>   // size_t

/**
 * ============================================================================
 *              W3 嵌入式工程手撕专项：对齐内存分配 aligned_malloc
 * ============================================================================
 *
 * 【背景】
 *   标准库 malloc(n) 只保证满足 C 标准规定的最小对齐（通常 8 或 16 字节），
 *   无法保证用户指定的任意对齐（如 4 / 16 / 32 字节）。
 *   嵌入式场景中，DMA 缓冲区、Cache line、SIMD/硬件加速器常要求更高对齐，
 *   因此需要自行实现「返回对齐地址 + 能正确释放」的分配器。
 *
 * 【对应周计划】
 *   - 26秋招周计划细化.md → W3 嵌入式专项 / 附录 C.9
 *   - 考点：DMA/Cache 对齐；free 必须释放原始 malloc 指针
 *
 * 【核心思路（四步）】
 *   1. 向 malloc 多申请一块冗余空间（size + alignment + sizeof(void*)）
 *   2. 在冗余空间内计算满足 alignment 的起始地址
 *   3. 在对齐地址「紧前面」隐藏 raw 指针（cookie），供 free 时使用
 *   4. aligned_free 从 cookie 取出 raw，再调用 free(raw)
 *
 * 【内存布局示意】
 *
 *   malloc 返回的 raw ─────────────────────────────────────────────────────►
 *          │                              │← cookie →│←── 用户 size ──►│
 *          raw                        (void*)[-1]   aligned（返回给用户）
 *                                     存 raw 地址
 *
 * 【前置条件 / 避坑】
 *   - alignment 必须是 2 的幂（4、8、16、32…），否则位掩码 ~(alignment-1) 无效
 *   - 释放时必须用 aligned_free(ptr)，禁止直接 free(ptr)
 *   - 标准库替代：Windows _aligned_malloc/_aligned_free；POSIX posix_memalign
 *
 * 【口述题（面试 ≥1 分钟）】
 *   1. DMA 缓冲区为何常需 4/16/32 字节对齐？非对齐访问会怎样？
 *   2. 为什么不能直接 free(aligned_ptr)？
 *   3. 多分配的 alignment + sizeof(void*) 各自解决什么问题？
 *
 * 【编译运行（可选自测）】
 *   g++ -std=c++17 内存malloc和free.cpp -o aligned_test && ./aligned_test
 * ============================================================================
 */

/**
 * ============================================================================
 * 【完整数值推演示例】
 * ============================================================================
 *
 * 假设（64 位 Linux / Windows，sizeof(void*) = 8）：
 *   aligned_malloc(100, 16)
 *   即：用户要 100 字节，且返回地址必须是 16 的倍数（DMA 常用 16 字节对齐）
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 第 1 步：malloc 多要空间
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   申请字节数 = size + alignment + sizeof(void*)
 *              = 100 + 16 + 8
 *              = 124 字节
 *
 *   假设堆管理器返回 raw = 0x1003（故意设成非 16 对齐，说明算法不依赖 raw 对齐）
 *
 *   此时堆块范围（逻辑上）：
 *   ┌──────────────────────────────────────────────────────── raw+124 ────►
 *   │ 0x1003                                              ... 0x107E │
 *   └ raw                                                              ┘
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 第 2 步：计算对齐地址
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   addr = (uintptr_t)raw + sizeof(void*) + alignment
 *        = 0x1003 + 8 + 16
 *        = 0x101B
 *
 *   对齐掩码（alignment = 16 = 0x10）：
 *     alignment - 1   = 0x0F  （低 4 位为 1）
 *     ~(alignment - 1) = ...FFFFFFF0  （低 4 位为 0，用于 floor 对齐）
 *
 *   aligned = addr & ~(alignment - 1)
 *           = 0x101B & ~0x0F
 *           = 0x1010        ← 0x1010 % 16 == 0，满足对齐
 *
 *   【为何不能「从 raw 直接对齐」？为何要 +alignment 再向下对齐？】
 *
 *   硬约束：cookie 写在 aligned 前面 8 字节，即地址 (aligned - 8)。
 *           这 8 字节必须落在 malloc 块内部 →  aligned - 8 >= raw  →  aligned >= raw + 8
 *
 *   ── 反例 1：从 raw 本身向上对齐（没留 cookie 空间）──
 *     raw = 0x1009，alignment = 16
 *     若 aligned = 向上取整(raw) = 0x1010
 *     cookie 应在 0x1010 - 8 = 0x1008
 *     但 0x1008 < raw(0x1009)  →  cookie 落在堆块**外面**（写到 malloc 头之前）→ 崩溃/堆损坏
 *
 *   ── 反例 2：只加 8 字节再「向下」对齐（本实现用的是向下对齐 & ~mask）──
 *     raw = 0x1009，从 raw+8 = 0x1011 向下对齐 → aligned = 0x1010
 *     cookie 仍在 0x1008 < 0x1009  →  一样越界！
 *     （若改成「向上对齐」raw+8 则可以，见文末另一种写法）
 *
 *   ── 本实现的做法：raw + 8 + alignment，再向下对齐 ──
 *     先大步往前跳 alignment，再 floor 对齐，最坏情况也有：
 *       aligned >= raw + 8 + 1 > raw + 8
 *     因此 cookie 地址 aligned-8 一定 >= raw，不会写到堆外。
 *
 *     对本例 raw=0x1003：addr=0x101B → aligned=0x1010 → cookie=0x1008 >= 0x1003 ✓
 *
 *   ── 补充：另一种常见写法（效果等价，面试也认可）──
 *     start = raw + 8;
 *     aligned = (start + alignment - 1) & ~(alignment - 1);  // 向上对齐，不是向下
 *     对本例同样得到 0x1010；对 raw=0x1009 得到 0x1020，cookie=0x1018 ✓
 *     周计划 C.9 选用「+alignment 再向下」写法，与向上取整 raw+8 数学上等价安全。
 * * ───────────────────────────────────────────────────────────────────────────
 * 第 3 步：写入 cookie，返回用户指针
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   内存布局（地址从低到高）：
 *
 *   地址        内容                         说明
 *   ─────────────────────────────────────────────────────────────
 *   0x1003      （堆块第 1 字节）            raw = malloc 返回的起始地址，不是「占 8 字节的字段」
 *   0x1004      （对齐前的空隙）             ┐
 *   0x1005      （对齐前的空隙）             │ 头部空隙 / padding，共 5 字节
 *   0x1006      （对齐前的空隙）             │ 区间 [0x1003, 0x1008)，不含 0x1008
 *   0x1007      （对齐前的空隙）             ┘ 算法算出的 cookie 在 0x1008，此前字节闲置
 *   0x1008      cookie 第 1 字节             ┐
 *   0x1009      cookie 第 2 字节             │
 *   0x100A      cookie 第 3 字节             │ cookie 共 8 字节（一个 void*）
 *   0x100B      cookie 第 4 字节             │ 区间 [0x1008, 0x1010) 左闭右开
 *   0x100C      cookie 第 5 字节             │ 即占用 0x1008～0x100F，不含 0x1010
 *   0x100D      cookie 第 6 字节             │
 *   0x100E      cookie 第 7 字节             │
 *   0x100F      cookie 第 8 字节             ┘ 整段存 raw = 0x0000000000001003
 *               ↑ ((void**)0x1010)[-1] 指向这块的起始地址 0x1008
 *   0x1010      用户区 [0]                   aligned，返回给用户的指针 p（cookie 之后第一字节）
 *   0x1011      用户区 [1]
 *   ...
 *   0x1073      用户区 [99]                  共 100 字节，0x1010 + 100 - 1
 *   ...
 *   0x107E      （堆块最后一字节）           共 124 字节，区间 [0x1003, 0x107F) 左闭右开
 *
 *   【为何 8 字节从 0x1008「到」0x1010？】
 *     内存区间习惯用左闭右开 [起始, 起始+长度)：
 *       0x1008 + 8 = 0x1010  →  cookie 占 [0x1008, 0x1010)
 *     实际占用的最后一字节是 0x100F，不是 0x1010。
 *     0x1010 是用户区的起点，与 cookie 相邻但不重叠。
 *     手算：0x1008,1009,100A,100B,100C,100D,100E,100F → 数一下正好 8 个地址。
 *
 *   【raw 占几个字节？】
 *     要分两层理解：
 *     (1) 变量 raw（代码里的 void*）：在 64 位 CPU 上这个指针变量本身占 8 字节，在栈/寄存器里，
 *         和堆上布局无关。
 *     (2) 堆块起点 raw（地址 0x1003）：标记 malloc 交给 free 的那块内存从哪里开始，
 *         不是单独再划一块「raw 区」。从 0x1003 到 cookie 前 0x1007 是 5 字节 padding，
 *         没有存用户数据，属于为对齐和 cookie 位置付出的开销。
 *     整块 malloc 申请量 = 124 字节 = [0x1003, 0x107F)
 *       = padding(5) + cookie(8) + 用户(100) + 尾部余量(11)
 *     尾部余量 [0x1074, 0x107F) 也是冗余，保证用户 100 字节绝不越界。
 *
 *   代码：((void**)aligned)[-1] = raw;
 *         在 0x1010 处视为 void* 数组，下标 -1 → 0x1008，写入 0x1003
 *
 *   校验对齐：
 *     (uintptr_t)p & (16 - 1) = 0x1010 & 0x0F = 0  ✓
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 第 4 步：aligned_free 释放
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   用户调用：aligned_free(p);   // p = 0x1010
 *
 *   1. ptr 非空，进入释放逻辑
 *   2. ((void**)ptr)[-1] 从 0x1008 读出 cookie → 0x1003 = raw
 *   3. free(0x1003)  ← 必须 free 这个地址，堆管理器才能正确回收整块 124 字节
 *
 *   若错误地 free(p) 即 free(0x1010)：
 *     堆管理器认为 0x1010 才是块头，但 0x1010 不是 malloc 返回地址
 *     → 未定义行为：崩溃、堆损坏、静默内存泄漏
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 【补充示例】alignment = 32 时掩码如何工作
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   aligned_malloc(64, 32)，raw = 0x2005
 *
 *   addr    = 0x2005 + 8 + 32 = 0x202D
 *   aligned = 0x202D & ~0x1F = 0x2020     （0x2020 % 32 == 0）
 *   cookie  = 0x2020 - 8 = 0x2018        （写入 raw = 0x2005）
 *   用户区  = [0x2020, 0x2020 + 63] = [0x2020, 0x205F]
 *
 *   32 字节对齐在 Cortex-M7 + DMA + Cache line 场景很常见。
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 【位运算手算对照表】alignment = 16
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   addr (十六进制)    addr & 0x0F (未对齐余量)    aligned = addr - 余量
 *   ─────────────────────────────────────────────────────────────
 *   0x1010             0                          0x1010  （已对齐）
 *   0x101B             0xB (11)                   0x1010
 *   0x1025             0x5 (5)                    0x1020
 *   0x1030             0x0                        0x1030
 *
 *   规律：aligned 永远是 ≤ addr 的最大 16 倍数。
 *
 * ───────────────────────────────────────────────────────────────────────────
 * 【冗余空间账算】为何 size + alignment + sizeof(void*) 一定够？
 * ───────────────────────────────────────────────────────────────────────────
 *
 *   最坏情况：从 raw 往后，cookie 占 8 字节，对齐最多浪费 15 字节（alignment-1），
 *   再往后才是用户 size 字节。
 *   总需求 ≤ sizeof(void*) + (alignment - 1) + size
 *          <  sizeof(void*) + alignment + size     ← 与申请量一致，有余量 ✓
 * ============================================================================
 */

/**
 * @brief 分配一块起始地址按 alignment 对齐的内存
 *
 * @param size      用户需要的字节数
 * @param alignment 对齐字节数，必须是 2 的幂（如 16、32）
 * @return 对齐后的指针；失败返回 NULL
 *
 * 实现要点：在对齐地址前一个 void* 位置保存 raw 指针，供 aligned_free 找回。
 */
void *aligned_malloc(size_t size, size_t alignment)
{
    // -------------------------------------------------------------------------
    // 第 1 步：向系统多申请冗余空间
    // -------------------------------------------------------------------------
    // malloc 只返回「某块堆内存的起始地址 raw」，不保证 raw 本身满足 alignment。
    // 额外空间用途：
    //   - sizeof(void*) ：在对齐地址前存 raw 指针（cookie）
    //   - alignment     ：最多浪费 alignment-1 字节，才能「滑到」下一个对齐边界
    // 因此总申请量 = size + alignment + sizeof(void*)
    void *raw = malloc(size + alignment + sizeof(void *));
    if (!raw) {
        return NULL;  // 堆耗尽或分配失败，与 malloc 语义一致
    }

    // -------------------------------------------------------------------------
    // 第 2 步：在 raw 之后找第一个合法的对齐地址
    // -------------------------------------------------------------------------
    // 先把 raw 转成整数地址 uintptr_t，便于做位运算（指针不能直接 &、+ 对齐掩码）。
    //
    // addr = raw + sizeof(void*) + alignment 的含义：
    //   - 跳过 raw 起始处 sizeof(void*) 字节，预留给 cookie（在对齐地址 [-1] 处）
    //   - 再跳过一整段 alignment，保证后续「向下对齐」后，[-1] 仍在 raw 块内部
    //
    // 若写成 raw + sizeof(void*) 再 (addr + alignment - 1) & mask 也是常见写法，
    // 本实现与周计划附录 C.9 保持一致。
    uintptr_t addr = (uintptr_t)raw + sizeof(void *) + alignment;

    // 向下取整到 alignment 的整数倍（alignment 须为 2 的幂）：
    //   alignment - 1  的低 log2(alignment) 位全为 1，其余为 0
    //   ~(alignment-1) 是「对齐掩码」，低位清零即完成 floor 对齐
    // 例：alignment=16(0x10)，addr=0x1025 → aligned=0x1020
    uintptr_t aligned = addr & ~(alignment - 1);

    // -------------------------------------------------------------------------
    // 第 3 步：在对齐地址紧前面写入 raw（cookie）
    // -------------------------------------------------------------------------
    // (void**)aligned 把 aligned 视为「void* 数组」的首元素；
    // [-1] 表示往前偏移一个 void*（32 位机 4 字节，64 位机 8 字节），
    // 该位置不在用户可见范围内，专门存放 malloc 原始返回值。
    // 释放时：free(((void**)ptr)[-1]) 才能正确归还整块堆内存。
    ((void **)aligned)[-1] = raw;

    // -------------------------------------------------------------------------
    // 第 4 步：返回对齐后的地址给用户
    // -------------------------------------------------------------------------
    // 用户从 aligned 起连续 size 字节可用；对齐检查：
    //   assert(((uintptr_t)p & (alignment - 1)) == 0);
    return (void *)aligned;
}

/**
 * @brief 释放由 aligned_malloc 返回的指针
 *
 * @param ptr aligned_malloc 的返回值；允许传入 NULL（与 free 行为一致）
 *
 * 切勿对 ptr 直接调用 free(ptr)——堆管理器需要的是 malloc 返回的 raw，而非对齐后的地址。
 */
void aligned_free(void *ptr)
{
    if (ptr) {
        // 从 cookie 取出 raw，再交给标准 free
        // ((void**)ptr)[-1] 与 aligned_malloc 里写入的位置一一对应
        free(((void **)ptr)[-1]);
    }
}

#ifdef ALIGNED_MALLOC_SELF_TEST
#include <cassert>
#include <cstdio>

int main()
{
    const size_t align = 32;
    const size_t size = 100;

    void *p = aligned_malloc(size, align);
    assert(p != nullptr);
    assert(((uintptr_t)p & (align - 1)) == 0);

    // 写入不应越界或 crash
    unsigned char *bytes = (unsigned char *)p;
    for (size_t i = 0; i < size; ++i) {
        bytes[i] = (unsigned char)(i & 0xFF);
    }

    printf("aligned_malloc OK: ptr=%p, alignment=%zu\n", p, align);
    aligned_free(p);
    return 0;
}
#endif
